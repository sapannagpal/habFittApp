// ─── Helper to build a stub exercise for non-Week-1 sessions ─────────────────

function stubExercise(id, name, muscle) {
  return {
    id,
    name,
    primaryMuscle: muscle,
    secondaryMuscles: [],
    sets: 3,
    reps: '10-12',
    defaultWeightKg: 0,
    notes: 'Progressive overload — increase weight from previous week.',
  };
}

function stubSession(id, dayLabel, name, minutes, muscles) {
  return {
    id,
    dayLabel,
    name,
    estimatedMinutes: minutes,
    muscleGroups: muscles,
    exercises: [stubExercise(`${id}_ex1`, 'Progressive Overload Carry-over', muscles[0])],
    isRestDay: false,
    defaultRestSeconds: 90,
  };
}

function restSession(id, dayLabel) {
  return {
    id,
    dayLabel,
    name: 'Rest Day',
    estimatedMinutes: 0,
    muscleGroups: [],
    exercises: [],
    isRestDay: true,
    defaultRestSeconds: 0,
  };
}

